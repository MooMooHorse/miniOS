/**
 * @brief from file raw data RGB 5:6:5 to extracted image RGB 5:6:5 with
 * width w and height h, centering at center_x,center_y(those are w.r.t. raw image)
 * Raw image is too large, hence this function
 * @param fd - fd to read
 * @param p  - p  to store
 * @param ux  - upper left coordinate
 * @param uy  - upper left coordinate
 * @param w - result width
 * @param h - result height
 * @return ** int32_t - read success or not
 */
int32_t 
draw_img(int32_t fd,image_t* p,int32_t ux,int32_t uy, int32_t w,int32_t h){
    /* read from fd a image center at center_x, center_y */
    int32_t i,j,x,y;
    uint16_t read_val;
    if(!coordinate_check(ux+w-1,uy+h-1,p->hdr.width,p->hdr.height)
    ||!coordinate_check(w-1,h-1,IMAGE_X_DIM,IMAGE_Y_DIM)) return -1;
    y=0;
    for(i=0;i<p->hdr.height;i++){
        x=0;
        for(j=0;j<p->hdr.width;j++){
            if(-1==ece391_read(fd,&read_val,sizeof(read_val))){
                return -1;
            }
            if(i>=uy&&j>=ux&&j<ux+w&&i<uy+h){
            // current (i,j) falls within expected region (center_x,center_y,w,h)
                p->img[y][x++]=read_val;
                if(p->shallowing) p->img[y][x-1]=shallow_RGB(p,read_val);
            }
        }
        if(i>=uy&&i<uy+h){
            y++;
            if(x!=w){// sanity check
                return -1;
            }
        }
    }
    if(y!=h){// sanity chec
        return -1;
    }
    reverse_img(p);
    p->present=1;
    p->hdr.height=h;
    p->hdr.width=w;
    return 0;
}

/**
 * @brief Text to Graphics. Convert a string to graphics. 
 * @param src - source string, should be const 
 * @param n - length of src, which shouldn't always 
 * @param p - output object
 * @return unsigned char*, a pointer to the text buffer
 */
int32_t
draw_text(const char* src,int n,text_t* p,int32_t scale){
    int32_t r=0,lines=0;
    int32_t i,j,a,b,mx_w=0,k=0;
    /* extrac each non-empty line of text : non-empty line number lines */
    for(i=0;i<n;i++){
        if(src[i]=='\n'){
            line_len[lines]=r;
            if(r>mx_w) mx_w=r;
            lines++;
            r=0;
        }
        else{
            line[lines][r++]=src[i];
            if(r>=TEXT_X_DIM/FONT_WIDTH) return -1; /* text overflow */
        }
    }
    line_len[lines]=r;
    if(r>mx_w) mx_w=r;
    lines++;

    if(lines>=MAX_LINES_IN_BOX) return -1; /* too many lines (only 4 scales(lines) are supported) */
    if(lines>scale) return -1; /* with this scale and this line number, text box will overflow */
    
    memset(p->img,0,sizeof(p->img));

    for(i=0;i<lines;i++){
        for(j=0;j<line_len[i];j++){
            for(a=0;a<16;a+=scale){
                int extract_bit=1<<DECODE_PIXEL_LEN;
                for(b=0;b<DECODE_PIXEL_LEN;b+=scale){
                    extract_bit>>=scale;
                    if(scale==1){
                        p->img[(i+1)*PADDING_HEIGHT+i*(FONT_HEIGHT/scale)+a/scale]
                        [PADDING_WIDTH+j*(FONT_WIDTH/scale)+(b/scale)]=
                        ((font_data[(int)src[k]][a]&extract_bit)>0?
                        p->text_col:p->filled_col);
                    }
                    if(scale==2){
                        p->img[(i+1)*PADDING_HEIGHT+i*(FONT_HEIGHT/scale)+a/scale]
                        [PADDING_WIDTH+j*(FONT_WIDTH/scale)+(b/scale)]=
                        (((font_data[(int)src[k]][a]|font_data[(int)src[k]][a+1])&extract_bit)>0?
                        p->text_col:p->filled_col);
                    }
                }
                /* fill front back padding color */
                for(b=0;b<PADDING_WIDTH;b++){
                    p->img[(i+1)*PADDING_HEIGHT+i*(FONT_HEIGHT/scale)+a/scale][b]=p->filled_col;
                }
                for(b=PADDING_WIDTH+line_len[i]*(FONT_WIDTH/scale);
                b<PADDING_WIDTH*2+mx_w*(FONT_WIDTH/scale);b++){
                    p->img[(i+1)*PADDING_HEIGHT+i*(FONT_HEIGHT/scale)+a/scale][b]=p->filled_col;
                }
            }
            k++;
        }
    }

    /* add line padding color */
    for(i=0;i<=lines;i++){
        for(j=0;j<mx_w*FONT_WIDTH/scale+2*PADDING_WIDTH;j++){
            p->img[i*PADDING_HEIGHT+i*(FONT_HEIGHT/scale)][j]=p->filled_col;
        }
    }

    p->hdr.height=FONT_HEIGHT/scale*lines+PADDING_HEIGHT*2;
    p->hdr.width=FONT_WIDTH/scale*mx_w+PADDING_WIDTH*2;
    p->present=1;
    return 0;
}

