inline int32_t shallow_RGB(image_t* p,int32_t read_val){
    int32_t R,G,B;
    B=read_val&0x1f;read_val>>=5;
    G=read_val&0x3f;read_val>>=6;
    R=read_val&0x1f;
    R=(0x1f-R)*p->shallowing/100+R;
    G=(0x3f-G)*p->shallowing/100+G;
    B=(0x1f-B)*p->shallowing/100+B;
    return B+(G<<5)+(R<<11);
}


